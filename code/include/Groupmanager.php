<?php

require_once PATH_INCLUDE . '/Group.php';

/**
 * Manages the Groups
 *
 * @author  Pascal Ernst <pascal.cc.ernst@gmail.com>
 */
class Groupmanager {

	/////////////////////////////////////////////////////////////////////
	//Constructor
	/////////////////////////////////////////////////////////////////////

	public function __construct() {

		$this->groupsLoad();
	}

	/////////////////////////////////////////////////////////////////////
	//Methods
	/////////////////////////////////////////////////////////////////////

	public function groupRootGet() {

		return $this->_groupRoot;
	}

	/////////////////////////////////////////////////////////////////////
	//Implements
	/////////////////////////////////////////////////////////////////////

	protected function groupsLoad() {

		$data = Group::groupsLoad();
		$this->_groupRoot = $data[1];
	}

	/////////////////////////////////////////////////////////////////////
	//Attributes
	/////////////////////////////////////////////////////////////////////

	/**
	 * The Root-Element of the Group-Tree; contains all other groups as childs
	 * @var Group
	 */
	protected $_groupRoot;
}

?>
