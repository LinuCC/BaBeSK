<?php

require_once PATH_INCLUDE . '/NModule.php';

/**
 * The new Modulemanager
 *
 * @author  Pascal Ernst <pascal.cc.ernst@gmail.com>
 */
class NModulemanager {

	/////////////////////////////////////////////////////////////////////
	//Constructor
	/////////////////////////////////////////////////////////////////////

	public function __construct() {

		$this->modulesLoad();
	}

	/////////////////////////////////////////////////////////////////////
	//Methods
	/////////////////////////////////////////////////////////////////////

	public function moduleRootGet() {

		return $this->_moduleRoot;
	}

	/////////////////////////////////////////////////////////////////////
	//Implements
	/////////////////////////////////////////////////////////////////////

	protected function modulesLoad() {

		$modArray = NModule::modulesLoad();
		$this->_moduleRoot = $modArray[1];
	}

	/////////////////////////////////////////////////////////////////////
	//Attributes
	/////////////////////////////////////////////////////////////////////

	/**
	 * The Root-Element of the Group-Tree; contains all other groups as childs
	 * @var Group
	 */
	protected $_moduleRoot;
}

?>
