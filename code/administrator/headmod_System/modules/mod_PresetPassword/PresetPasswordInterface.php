<?php

require_once PATH_ADMIN . '/AdminInterface.php';

class PresetPasswordInterface extends AdminInterface {
	/////////////////////////////////////////////////////////////////////
	//Constructor
	/////////////////////////////////////////////////////////////////////

	public function __construct ($modPath, $smarty) {
		parent::__construct ($modPath, $smarty);
		$this->parentPath = $this->tplFilePath . 'header.tpl';
		$this->smarty->assign('inh_path', $this->parentPath);
	}

	/////////////////////////////////////////////////////////////////////
	//Methods
	/////////////////////////////////////////////////////////////////////

	public function mainMenuShow ($flcp) {
		$this->smarty->assign ('onFirstLoginChangePassword', $flcp);
		$this->smarty->display ($this->tplFilePath . 'changePresetPassword.tpl');
	}

	/////////////////////////////////////////////////////////////////////
	//Implements
	/////////////////////////////////////////////////////////////////////

	/////////////////////////////////////////////////////////////////////
	//Attributes
	/////////////////////////////////////////////////////////////////////
}
?>