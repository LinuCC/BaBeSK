<?php

require_once PATH_INCLUDE . '/DataContainer.php';

class ContractDataContainer extends DataContainer {
	////////////////////////////////////////////////////////////////////////////////
	//Constructor
	////////////////////////////////////////////////////////////////////////////////
	public function __construct($smarty, $interface, $languageManager) {
		parent::__construct($smarty, $interface);
		$this->_languageManager = $languageManager;
	}
	////////////////////////////////////////////////////////////////////////////////
	//Getters and Setters
	////////////////////////////////////////////////////////////////////////////////
	public function getLanguageManager () {
		return $this->_languageManager;
	}
	////////////////////////////////////////////////////////////////////////////////
	//Methods
	////////////////////////////////////////////////////////////////////////////////
	
	////////////////////////////////////////////////////////////////////////////////
	//Implementations
	////////////////////////////////////////////////////////////////////////////////
	
	////////////////////////////////////////////////////////////////////////////////
	//Attributes
	////////////////////////////////////////////////////////////////////////////////
	protected $_languageManager;
}
?>